          pointHoverBorderColor: "rgba(78, 115, 223, 1)",
          pointHitRadius: 10,
          pointBorderWidth: 2,
          data: {!! $widget['transaksi_perbulan']->pluck('count') !!},
        }]
      },
      options: {
        maintainAspectRatio: false,
        layout: {
